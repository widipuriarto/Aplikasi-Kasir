    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(txtItemCode.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please select the data you want to delete!");
        }else{
            int jawab = JOptionPane.showConfirmDialog(null, "Are you sure?", "Confirmation", JOptionPane.YES_NO_OPTION);
            if(jawab == 0){
                try {
                    st = conn.createStatement();
                    String sql = "DELETE FROM barang WHERE kodeBarang = '" + txtItemCode.getText() + "'";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data successfully deleted!");
                    TampilData();
                    Clear();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

