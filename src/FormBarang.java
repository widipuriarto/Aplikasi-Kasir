    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
            st = conn.createStatement();

            // Validasi data tidak boleh kosong
            if (txtItemCode.getText().equals("") ||
                txtItemName.getText().equals("") ||
                txtAreaDescription.getText().equals("") ||
                txtPrice.getText().equals("")) {

                JOptionPane.showMessageDialog(null, "Data must not be empty", "Data Validation", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            String kode = txtItemCode.getText();
            String nama = txtItemName.getText();
            String deskripsi = txtAreaDescription.getText();
            int harga = Integer.parseInt(txtPrice.getText());

            if (!txtPrice.getText().matches("\\d+")) {
                JOptionPane.showMessageDialog(null, "Price must be a number");
                return;
            }

            // aksi update data
            String sql = "UPDATE barang SET "
            + "namaBarang = '" + nama + "', "
            + "deskripsi = '" + deskripsi + "', "
            + "hargaJual = '" + harga + "' "
            + "WHERE kodeBarang = '" + kode + "'";
            st.execute(sql);
            JOptionPane.showMessageDialog(null, "Data successfully changed!");
            Clear();
            TampilData();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "An error occurred: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

